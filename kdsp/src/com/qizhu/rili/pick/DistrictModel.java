public class DistrictModel {
	private String name;

	public DistrictModel() {
		super();
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "DistrictModel [name=" + name + "]";
	}

}
